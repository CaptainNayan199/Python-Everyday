# There are many operator in python as like in other programming languages.
# Some of it includes : + , - , * , / , // , % , **, etc.

# Lets see some operator in brief: 

# Adition operator (+)
a = 6
b =12
sum = a+b
print(sum)


# Subtraction Operator (-)
a = 12
b = 4
difference = a-b
print(difference)

# Multiply operator (*)
a = 3
b = 4
product = a*b
print(product)


# Division operator (/)
a = 6
b = 2
division = a/b
print(division)


# Floor division operator (//)
a = 15
b = 4
floor = a//b
print(floor)


# modulo/remainder operator (%)
a=7
b=3
mod= a%b
print(mod)

# Exponential operator (**)
a = 2
b = 4
exp = (a**b)
print(exp) #16

